package warhorn.example.domain;

public class Event {
  private String id;
  private String title;

  public String getId() {
    return id;
  }

  public String getTitle() {
    return title;
  }

  public void setId(String id) {
    this.id = id;
  }

  public void setTitle(String title) {
    this.title = title;
  }
}
